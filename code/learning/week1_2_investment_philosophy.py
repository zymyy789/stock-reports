print("""
================================================================================
第二章: 巴菲特投资哲学进化
================================================================================

【巴菲特的进化路径】

第一阶段 (1950s-1960s): 捡烟蒂
  → 继承格雷厄姆方法
  → 买便宜的公司 (PB<0.5, PE<5)
  → 清仓式分散 (100+只股票)
  → 烟蒂投资法

第二阶段 (1960s-1970s): 转型优质公司
  → 遇到查理芒格
  → "用合理价格买优秀公司 > 用便宜价格买普通公司"
  → 开始重仓少数优质公司

第三阶段 (1980s-现在): 护城河投资
  → 形成"经济护城河"理论
  → 长期持有少数伟大公司
  → 集中持仓 (前10大占80%+)

【巴菲特最核心的3个思想】

1. "买股票就是买公司的一部分"
   - 不要看股价波动
   - 关注公司盈利能力
   - 用做生意的心态投资

   实操:
   - 我持有招商银行 = 我持有银行的一部分
   - 每年分红 = 公司给我分红
   - 如果银行每年ROE=13%，长期我会赚钱

2. "能力圈原则"
   - 只投资你懂的公司
   - 不懂的不碰
   - 科技股 → 巴菲特很长时间不碰 (直到看懂苹果)

   我的能力圈:
   - 银行 ✅
   - 保险 ✅
   - 消费/白酒 ✅
   - 科技 ❌
   - 医药 ❌
   → 所以我目前不碰科技股是对的

3. "时间是优秀公司的朋友，是平庸公司的敌人"
   - 好公司拿10年会怎样?
   - 招行: ROE=13%，长期复合增长10%+
   - 10年后招行利润可能是现在的2.6倍
   - 股价自然上涨

   反面例子:
   - 买垃圾股 → 时间是敌人
   - 越久越危险

【巴菲特的风险观】

普通人的风险观:
  风险 = 股价下跌

巴菲特的风险观:
  风险 = 本金永久损失的可能性

区别:
  - 股价下跌20%，但公司基本面没变 → 不是风险
  - 股价下跌20%，公司基本面恶化 → 是风险

这意味着:
  - 短期波动不是风险
  - 买贵了才是风险
  - 买错公司才是风险

【巴菲特的估值方法】

绝对估值:
  DCF = 未来现金流折现
  → 但巴菲特很少用DCF精确计算

相对估值:
  "用合理价格买优秀公司"
  → 关键是"合理价格"怎么判断

巴菲特的方法:
  1. 内在价值 = 未来10年公司能赚的总和
  2. 打折买 (安全边际)
  3. 打折多少? 至少25-30%

  例子:
  公司每年赚10亿，10年赚100亿
  → 如果价格 < 70亿，就值得买
  → 30亿的安全边际

【巴菲特选股标准(简化版)】

1. 业务简单，容易理解
   - 卖可乐/剃须刀/糖果 → 能理解
   - 复杂金融衍生品 → 不能理解

2. 长期稳定的盈利能力
   - ROE > 15%
   - 利润不是靠一次性收益

3. 有护城河
   - 品牌/专利/网络效应/低成本
   - 别人很难抢走生意

4. 管理层诚实有能力
   - 巴菲特看人
   - 我目前没有能力评估管理层

5. 价格合理
   - 有安全边际
   - 不是必须在最低点买，但要合理

我的持仓对照:
  招行: 业务简单 ✅ ROE15% ✅ 有护城河 ✅ 价格合理 ✅
  平安: 业务复杂 ⚠️ ROE14% ✅ 有护城河 ✅ 价格很低 ✅
""")

