print("\n【PEG分析 - 彼得林奇方法】")
print("-"*50)
print("规则: PEG = PE / 净利润增长率")
print("      PEG < 1: 成长被低估 → 买入信号")
print("      PEG = 1-2: 成长合理")
print("      PEG > 2: 成长被高估 → 卖出信号")
print("      (注意: 周期股/金融股需要调整)")

def calc_peg(pe, growth):
    if growth <= 0:
        return 999
    return pe / growth

print("\n标准PEG计算:")
for name, d in STOCKS.items():
    peg = calc_peg(d['pe'], d['profit_growth_2025'])
    if peg < 1:
        interp = "🟢 被低估"
    elif peg < 2:
        interp = "🟡 合理"
    else:
        interp = "🔴 被高估"
    print(f"\n{name}:")
    print(f"  PE={d['pe']} / 增长{d['profit_growth_2025']}% = PEG={peg:.2f}")
    print(f"  解读: {interp}")

