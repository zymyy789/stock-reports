print("""
================================================================================
第二部分: K线基础 - 最核心的技术分析工具
================================================================================

K线 = 记录一段时间内的4个价格
  开盘价、收盘价、最高价、最低价

阳线(红色): 收盘 > 开盘 → 上涨
阴线(绿色): 收盘 < 开盘 → 下跌

关键K线形态:

1. 锤子线(底部反转信号)
   - 长下影线 + 短实体
   - 出现在下跌趋势中
   - 含义: 空头力量衰竭

2. 射击之星(顶部反转信号)
   - 长上影线 + 短实体
   - 出现在上涨趋势中
   - 含义: 多头力量衰竭

3. 十字星(犹豫信号)
   - 开盘≈收盘
   - 含义: 多空平衡，可能变盘

4. 大阳线(强势信号)
   - 实体较长，收盘远高于开盘
   - 含义: 多头强势

5. 大阴线(弱势信号)
   - 实体较长，收盘远低于开盘
   - 含义: 空头强势
================================================================================
""")

print("""
================================================================================
第三部分: 均线系统 - 判断趋势方向
================================================================================

均线(MA) = 过去N天的平均收盘价

常用均线:
  MA5: 5日均线（超短期）
  MA10: 10日均线（短期）
  MA20: 20日均线（月线，中期）
  MA60: 60日均线（季线，中长期）
  MA120: 120日均线（半年线，长期）
  MA250: 250日均线（年线，超长期）

均线判断标准:
  1. 多头排列: MA5>MA10>MA20>MA60 → 上升趋势
  2. 空头排列: MA5<MA10<MA20<MA60 → 下降趋势
  3. 金叉: 短均线上穿长均线 → 买入信号
  4. 死叉: 短均线下穿长均线 → 卖出信号

均线支撑/阻力:
  - 上升趋势中，股价回调到均线获得支撑
  - 下降趋势中，股价反弹到均线遇到阻力

我的理解:
  - 均线是"市场的平均成本"
  - 股价在均线上方 = 大部分人赚钱
  - 股价在均线下方 = 大部分人亏钱
  - MA250(年线)是最重要的长期趋势线
================================================================================
""")

print("""
================================================================================
第四部分: 成交量 - 价格的"燃料"
================================================================================

成交量 = 一段时间内的买卖数量

核心原则:
  量在价先 → 成交量变化领先价格变化

量价关系:
  1. 量增价升 → 健康上涨 ✅
  2. 量缩价升 → 上涨乏力 ⚠️
  3. 量增价跌 → 恐慌抛售 ❌
  4. 量缩价跌 → 下跌动能减弱 🔄

底部信号:
  - 地量见地价 → 成交量极度萎缩 = 底部特征
  - 放量上涨 → 资金进场 = 底部确认

顶部信号:
  - 天量见天价 → 成交量极度放大 = 顶部特征
  - 放量滞涨 → 大量成交但不涨 = 顶部信号

持仓股票成交量分析(概念):
  银行股: 成交量通常不大，说明关注度低
  → 低关注+低估 = 价值投资者的机会
  → 但也可能继续低关注，价格长期不涨
================================================================================
""")

print("""
================================================================================
第五部分: MACD - 最常用的技术指标
================================================================================

MACD = Moving Average Convergence Divergence
  均线收敛/发散指标

MACD组成:
  1. DIF线(快线): 12日EMA - 26日EMA
  2. DEA线(慢线): DIF的9日EMA
  3. MACD柱: (DIF - DEA) × 2

MACD判断标准:
  1. DIF上穿DEA → 金叉 → 买入信号
  2. DIF下穿DEA → 死叉 → 卖出信号
  3. DIF>0, DEA>0 → 多头市场
  4. DIF<0, DEA<0 → 空头市场
  5. MACD柱由负转正 → 底部信号
  6. MACD柱由正转负 → 顶部信号

MACD背离(最重要的信号):
  1. 底背离: 股价创新低，MACD不创新低
     → 下跌动能衰竭 → 即将反转
  2. 顶背离: 股价创新高，MACD不创新高
     → 上涨动能衰竭 → 即将反转

我的理解:
  MACD本质是"均线的变化速度"
  → 变化速度减慢 = 趋势可能要结束
  → 底背离是最可靠的技术信号之一
================================================================================
""")

