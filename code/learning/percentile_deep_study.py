print("""
================================================================================
第一部分: 什么是历史百分位？
================================================================================

历史百分位 = 当前PE在历史PE中的位置

例子:
  历史10年PE范围: 8 ~ 25倍
  当前PE: 12倍
  百分位 = (12 - 8) / (25 - 8) = 4/17 = 23.5%
  → 当前PE比历史上23.5%的时候高
  → 比历史上76.5%的时候低
  → 偏低估

百分位判断标准:
  < 20%: 极度低估 → 强烈买入信号
  20-40%: 低估 → 买入
  40-60%: 合理 → 持有
  60-80%: 高估 → 谨慎
  > 80%: 极度高估 → 卖出
================================================================================
""")

print("""
================================================================================
第二部分: 为什么历史百分位特别适合A股？
================================================================================

A股特点:
  1. 散户为主，情绪波动大
     → 经常出现极度高估/低估
     → 百分位指标更有效
  
  2. 政策影响大
     → 市场底部/顶部经常由政策决定
     → 历史会重演

  3. 行业轮动明显
     → 不同行业有不同周期
     → 百分位可以跨行业比较

  4. 机构占比低
     → 价格发现机制不如欧美
     → 错误定价更常见

为什么不是其他方法:
  DCF → 参数难确定
  PEG → 增长预测不准
  历史百分位 → 只看历史，不预测未来
================================================================================
""")

print("""
================================================================================
第三部分: 历史百分位的问题
================================================================================

问题1: 时间窗口选择
  - 10年数据: 包含完整牛熊周期
  - 5年数据: 更反映近期市场
  - 但如果行业结构变化，10年前的PE参考价值下降

问题2: "均值回归"假设
  - 历史百分位假设PE会回归历史均值
  - 但如果行业衰退，PE可能永远在低位
  - 例如: 报纸行业PE从15降到5，永远不会回去

问题3: 忽略基本面变化
  - 公司可能变得更好或更差
  - 单纯看历史分位可能出错

问题4: 极端情况
  - 当PE创历史新低时，百分位=0%
  - 但可能继续跌
  - 反之亦然

如何解决:
  1. 结合基本面分析
  2. 看绝对PE值 (PE<5或PE>50都是极端)
  3. 多只股票横向对比
================================================================================
""")

