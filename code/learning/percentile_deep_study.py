print("-"*70)

for s in stocks_analysis:
    pe = s['pe']
    lo, hi, avg = s['hist_low'], s['hist_high'], s['hist_avg']
    percentile = (pe - lo) / (hi - lo) * 100 if hi > lo else 50
    
    if percentile < 20:
        signal = "STRONG BUY"
    elif percentile < 40:
        signal = "BUY"
    elif percentile < 60:
        signal = "HOLD"
    elif percentile < 80:
        signal = "CAUTION"
    else:
        signal = "SELL"
    
    print(f"{s['name']:<12}{pe:<6.1f}{lo:<8.1f}{hi:<8.1f}{avg:<8.1f}{percentile:<8.0f}{signal}")

